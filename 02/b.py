a, b = 2, 7

# Arithmatic Ops
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(b ** a)

# Comparison Ops
print(a == b)
print(a != b)
print(a > b)
print(a >= b)
print(a < b)
print(a <= b)

# Logical Ops
if a == 2 and b == 7:
    print(True)

if a == 2 or b == 7:
    print(True)

if not(a == 2 or b == 7):
    print(True)
